"""
Напишите программу, которая получает целое число и возвращает его шестнадцатеричное строковое представление.
Функцию hex используйте для проверки своего результата.
"""

def int_to_hex(n):
    # Список символов для представления чисел в шестнадцатеричной системе
    hex_digits = "0123456789abcdef"

    # Если число нулевое, то сразу возвращаем "0x0"
    if n == 0:
        return "0x0"

    # Определяем, является ли число отрицательным
    negative = n < 0

    # Приводим число к положительному (модулю) для обработки
    n = abs(n)

    hex_str = ""

    # Пока число больше нуля
    while n > 0:
        remainder = n % 16  # Получаем остаток от деления на 16
        hex_str = hex_digits[remainder] + hex_str  # Добавляем соответствующий символ к результату
        n = n // 16  # Делим число на 16

    # Если число было отрицательным, добавляем минус
    if negative:
        return "-0x" + hex_str
    else:
        return "0x" + hex_str

# Пример использования
number = int(input("Введите целое число: "))
custom_hex = int_to_hex(number)
built_in_hex = hex(number)

print(f"Результат вашей функции: {custom_hex}")
print(f"Результат встроенной функции: {built_in_hex}")
print(f"Совпадают ли результаты: {custom_hex == built_in_hex}")
